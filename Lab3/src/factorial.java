public class factorial {
	public static void main(String[] args) {
		//this loop is for the segments and spacing 
		for ( int i = 5; i >= 1; i--) {
			// this loop prints out the digits
			for (int j = i; j <= 5; j++) {
				System.out.print(j);
			}
			System.out.println();
			System.out.println();
		}
	}
}
